package edu.cuny.util;

/**
 * <p>
 * Classes implementing this interface indicate that their status is resetable.
 * This is often used in jcat to re-initialize objects instead of
 * re-constructing them, with the associated garbage-collection and
 * initialization performance penalties.
 * </p>
 * 
 * @author Steve Phelps
 * @version $Revision: 1.9 $
 */

public interface Resetable {

	/**
	 * Reinitialize our status to the original settings.
	 */
	public void reset();

}